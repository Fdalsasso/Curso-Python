# Prueba las diferentes operaciones aritméticas (suma, resta, multiplicación, división) mostrando el resultado en pantalla.

variable1 = 10
variable2 = 2

print("10 + 2 = ", variable1 + variable2)
print("10 - 2 = ", variable1 - variable2)
print("10 * 2 = ", variable1 * variable2)
print("10 / 2 = ", variable1 / variable2)
print("10 ^ 2 = ", variable1 ** variable2)
print("El resultado entero de 10 / 2 es ", variable1 // variable2)